CREATE TEMPORARY TABLE JSON_PARSING AS 
SELECT
    JASON_SHIPPING:Customer_ID::VARCHAR        AS CUSTOMER_ID,
    JASON_SHIPPING:Shipping_ID::VARCHAR        AS SHIPPING_ID,
    JASON_SHIPPING:Status::VARCHAR         AS STATUS
