----Customers and their corresponding Orders and shipping count whose status are undeterministic
WITH MORE_THAN_ONE_ORDER AS (
SELECT DISTINCT  A.CUSTOMER_ID
FROM ASSESSMENT_DB.PUBLIC_RAW.CUSTOMER_DETAILS A
JOIN ASSESSMENT_DB.PUBLIC_RAW.ORDER_DETAILS B
ON A.CUSTOMER_ID = B.CUSTOMER_ID
GROUP BY A.CUSTOMER_ID
HAVING COUNT(DISTINCT ORDER_ID) >1


)
,  MORE_THAN_ONE_SHIPPING_ID AS (
SELECT DISTINCT A.CUSTOMER_ID
FROM MORE_THAN_ONE_ORDER  A
JOIN ASSESSMENT_DB.PUBLIC_RAW.SHIPPING_DETAILS_PARSED B
ON A.CUSTOMER_ID = B.CUSTOMER_ID
GROUP BY A.CUSTOMER_ID
--HAVING COUNT(DISTINCT SHIPPING_ID) >1

)
, SUMMARY AS (
SELECT A.CUSTOMER_ID,
COUNT(DISTINCT SHIPPING_ID) AS TOTAL_SHIPPING_IDS,
COUNT(DISTINCT ORDER_ID) AS TOTAL_ORDERS,
COUNT(DISTINCT CASE WHEN UPPER(TRIM(STATUS)) = 'DELIVERED' THEN SHIPPING_ID END) AS DELIVERED_SHIPPING_ID,
COUNT(DISTINCT CASE WHEN UPPER(TRIM(STATUS)) = 'PENDING' THEN SHIPPING_ID END) AS PENDING_SHIPPING_ID,
FROM MORE_THAN_ONE_SHIPPING_ID A
JOIN ASSESSMENT_DB.PUBLIC_RAW.SHIPPING_DETAILS_PARSED B 
ON A.CUSTOMER_ID = B.CUSTOMER_ID
JOIN ASSESSMENT_DB.PUBLIC_RAW.ORDER_DETAILS C
ON A.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY 1
)
---Orders which are undeterministic
SELECT CUSTOMER_ID,
TOTAL_ORDERS,
TOTAL_SHIPPING_IDS,
DELIVERED_SHIPPING_ID,
PENDING_SHIPPING_ID
FROM SUMMARY
WHERE NOT(DELIVERED_SHIPPING_ID=0 OR PENDING_SHIPPING_ID=0 OR DELIVERED_SHIPPING_ID=TOTAL_ORDERS)
ORDER BY CUSTOMER_ID;

